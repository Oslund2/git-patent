import { useState } from 'react';
import { FileText, ChevronDown, ChevronRight, ExternalLink } from 'lucide-react';

interface FilingGuideProps {
  expanded?: boolean;
}

interface SectionConfig {
  id: string;
  title: string;
  icon: React.ReactNode;
  accentFrom: string;
  accentTo: string;
  borderColor: string;
  badgeColor: string;
  iconBg: string;
  steps: { text: string; detail?: string }[];
}

const SECTIONS: SectionConfig[] = [
  {
    id: 'patent',
    title: 'Patent Filing',
    icon: <FileText className="w-5 h-5" />,
    accentFrom: 'from-patent-600',
    accentTo: 'to-blue-600',
    borderColor: 'border-l-patent-500',
    badgeColor: 'bg-patent-50 text-patent-700 border-patent-200',
    iconBg: 'bg-gradient-to-br from-patent-100 to-blue-100 text-patent-700',
    steps: [
      {
        text: 'Create an account at USPTO Patent Center',
        detail: 'patentcenter.uspto.gov',
      },
      {
        text: 'Select "New Provisional Application"',
        detail: 'This gives you 12 months to file the full (non-provisional) application.',
      },
      {
        text: 'Upload your specification PDF, claims, and drawings',
        detail: 'All of these documents have been generated by Git-Patent and are ready to use.',
      },
      {
        text: 'Pay the filing fee',
        detail: '$320 (micro entity) / $640 (small entity) / $1,600 (large entity). Most independent inventors and small startups qualify as micro or small entity.',
      },
      {
        text: 'Receive your provisional patent number',
        detail: 'You can now legally mark your product as "Patent Pending" to deter competitors.',
      },
      {
        text: 'Within 12 months: file a non-provisional application',
        detail: 'Convert your provisional to a full application for examination by a USPTO examiner. This is when the real patent process begins.',
      },
    ],
  },
  // Copyright and Trademark filing guides removed — coming as separate products
];

export function FilingGuide({ expanded = false }: FilingGuideProps) {
  const [openSections, setOpenSections] = useState<Set<string>>(
    expanded ? new Set(SECTIONS.map(s => s.id)) : new Set()
  );

  const toggleSection = (id: string) => {
    setOpenSections(prev => {
      const next = new Set(prev);
      if (next.has(id)) {
        next.delete(id);
      } else {
        next.add(id);
      }
      return next;
    });
  };

  return (
    <div className="space-y-4">
      <div className="flex items-center gap-3 mb-2">
        <div className="w-10 h-10 bg-gradient-to-br from-gray-100 to-slate-100 rounded-xl flex items-center justify-center">
          <ExternalLink className="w-5 h-5 text-gray-600" />
        </div>
        <div>
          <h3 className="text-lg font-bold text-gray-900">Filing Guide</h3>
          <p className="text-sm text-gray-500">Step-by-step instructions for filing your IP</p>
        </div>
      </div>

      {SECTIONS.map(section => {
        const isOpen = openSections.has(section.id);
        return (
          <div
            key={section.id}
            className={`bg-white border border-gray-100 rounded-2xl shadow-sm overflow-hidden transition-all border-l-4 ${section.borderColor}`}
          >
            <button
              onClick={() => toggleSection(section.id)}
              className="w-full flex items-center justify-between p-5 text-left hover:bg-gray-50/50 transition-colors"
            >
              <div className="flex items-center gap-3">
                <div className={`w-10 h-10 rounded-xl flex items-center justify-center ${section.iconBg}`}>
                  {section.icon}
                </div>
                <div>
                  <span className="font-semibold text-gray-900">{section.title}</span>
                  <span className="text-sm text-gray-400 ml-2">{section.steps.length} steps</span>
                </div>
              </div>
              {isOpen ? (
                <ChevronDown className="w-5 h-5 text-gray-400" />
              ) : (
                <ChevronRight className="w-5 h-5 text-gray-400" />
              )}
            </button>

            {isOpen && (
              <div className="px-5 pb-5 pt-0">
                <div className="space-y-4 ml-[52px]">
                  {section.steps.map((step, idx) => (
                    <div key={idx} className="flex gap-3">
                      <div className={`flex-shrink-0 w-7 h-7 rounded-full flex items-center justify-center text-xs font-bold border ${section.badgeColor}`}>
                        {idx + 1}
                      </div>
                      <div className="pt-0.5">
                        <p className="text-sm font-medium text-gray-800">{step.text}</p>
                        {step.detail && (
                          <p className="text-sm text-gray-500 mt-0.5 leading-relaxed">{step.detail}</p>
                        )}
                      </div>
                    </div>
                  ))}
                </div>
              </div>
            )}
          </div>
        );
      })}
    </div>
  );
}
